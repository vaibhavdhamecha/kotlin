// Kotlin Program to Add Two Float Number

fun main()
{
    var num1: Float
    var num2: Float
    var ans: Float

    println("Enter No 1: ")
    num1 = readln().toFloat()

    println("Enter No 2: ")
    num2 = readln().toFloat()

    ans = num1 + num2

    println("$num1 + $num2 = $ans")

}